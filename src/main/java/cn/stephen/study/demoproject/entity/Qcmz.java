package cn.stephen.study.demoproject.entity;

public class Qcmz {
    private int id;
    private int age;
    private String name;
    private String tel;
    private String peopleAddress;
    private String peopleRemarks;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getPeopleAddress() {
        return peopleAddress;
    }

    public void setPeopleAddress(String peopleAddress) {
        this.peopleAddress = peopleAddress;
    }

    public String getPeopleRemarks() {
        return peopleRemarks;
    }

    public void setPeopleRemarks(String peopleRemarks) {
        this.peopleRemarks = peopleRemarks;
    }
}
